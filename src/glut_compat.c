void glutSolidIcosahedron(void) {
    double t = (1.0 + sqrt(5.0)) / 2.0;
    GLfloat v[12][3] = {
        {-1, (GLfloat)t, 0}, {1, (GLfloat)t, 0}, {-1, -(GLfloat)t, 0}, {1, -(GLfloat)t, 0},
        {0, -1, (GLfloat)t}, {0, 1, (GLfloat)t}, {0, -1, -(GLfloat)t}, {0, 1, -(GLfloat)t},
        {(GLfloat)t, 0, -1}, {(GLfloat)t, 0, 1}, {-(GLfloat)t, 0, -1}, {-(GLfloat)t, 0, 1}
    };
    static const GLint f[20][3] = {
        {0, 11, 5}, {0, 5, 1}, {0, 1, 7}, {0, 7, 10}, {0, 10, 11},
        {1, 5, 9}, {5, 11, 4}, {11, 10, 2}, {10, 7, 6}, {7, 1, 8},
        {3, 9, 4}, {3, 4, 2}, {3, 2, 6}, {3, 6, 8}, {3, 8, 9},
        {4, 9, 5}, {2, 4, 11}, {6, 2, 10}, {8, 6, 7}, {9, 8, 1}
    };
    glBegin(GL_TRIANGLES);
    for (int i = 0; i < 20; i++) {
        float nx = (v[f[i][0]][0] + v[f[i][1]][0] + v[f[i][2]][0]) / 3.0;
        float ny = (v[f[i][0]][1] + v[f[i][1]][1] + v[f[i][2]][1]) / 3.0;
        float nz = (v[f[i][0]][2] + v[f[i][1]][2] + v[f[i][2]][2]) / 3.0;
        glNormal3f(nx, ny, nz);
        glVertex3fv(v[f[i][0]]);
        glVertex3fv(v[f[i][1]]);
        glVertex3fv(v[f[i][2]]);
    }
    glEnd();
}

void glutWireIcosahedron(void) {
    glPolygonMode(GL_FRONT_AND_BACK, GL_LINE);
    glutSolidIcosahedron();
    glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);
}

